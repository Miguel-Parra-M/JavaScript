    // Operadores de incremento y decremento

    /* 
    
    ++ Pre incremento
    Pos incremento ++
    -- Pre decremento
    Pos decremento --

    */

    let a=5;
    let b=6;
    let c;

    // Pre - Incremento
    console.log("El valor de \"a\" es: " + a);
    a= ++a;
    console.log("El valor de \"a\" ahora es: " + a);

    // Pos - Incremento
    console.log("El valor de \"b\" es: " + b);
    b++;
    c = b;
    console.log("El valor de \"b\" ahora es: " + c);

    // Pre - Decremento
    console.log("El valor de \"a\" es: " + a);
    a= --a;
    console.log("El valor de \"a\" ahora es: " + a);

    // Pos - Decremento
    console.log("El valor de \"b\" es: " + b);
    b--;
    c = b;
    console.log("El valor de \"b\" ahora es: " + c);
