    // Operadores Aritmeticos

    /* 

    + -> Suma - Convierte su operando a positivo
    - -> Resta - Convierte su operando a negativo
    * -> Multiplicación
    / -> Divición
    % -> Modulo o residuo
    ** -> Exponeciación
            
    */
    
    let a = 3;
    let b = 6;

    let Suma = a + b;
    console.log ("La suma es: " + Suma);

    let Resta = a - b;
    console.log ("La resta es:" + Resta);

    let Multi = a * b;
    console.log ("La multiplicación:" + Multi);

    let Div = a / b;
    console.log ("La división:" + Div);

    let Mod = a % b;
    console.log("El residuo de "+a+" entre "+b+ " es igual a:" + Mod);
    
    let Exp = a ** b;
    console.log("La exponenciación es: " + Exp);

